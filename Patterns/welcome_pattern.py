'''
Input: 7 21 
->  ---------.|.---------
    ------.|..|..|.------
    ---.|..|..|..|..|.---
    -------WELCOME-------
    ---.|..|..|..|..|.---
    ------.|..|..|.------
    ---------.|.---------
    
Input: 11 33
->  ---------------.|.---------------
    ------------.|..|..|.------------
    ---------.|..|..|..|..|.---------
    ------.|..|..|..|..|..|..|.------
    ---.|..|..|..|..|..|..|..|..|.---
    -------------WELCOME-------------
    ---.|..|..|..|..|..|..|..|..|.---
    ------.|..|..|..|..|..|..|.------
    ---------.|..|..|..|..|.---------
    ------------.|..|..|.------------
    ---------------.|.---------------
Input: 9 27
->  ------------.|.------------
    ---------.|..|..|.---------
    ------.|..|..|..|..|.------
    ---.|..|..|..|..|..|..|.---
    ----------WELCOME----------
    ---.|..|..|..|..|..|..|.---
    ------.|..|..|..|..|.------
    ---------.|..|..|.---------
    ------------.|.------------
'''
n, m = map(int, input().split())
for i in range(int((n-1)/2)):
    line = ((2*i+1)*'.|.').center(m, '-') #first half of pattern
    print(line)
    
print('WELCOME'.center(m,'-')) #middle line of pattern

for i in range(int((n-1)/2))[::-1]:
    line = ((2*i+1)*'.|.').center(m, '-') #second half of pattern
    print(line)
